package chawalit.chonpratatip.androidworkshop3;

public class Number {

    int number = 1;

    void increseNumber(){
        this.number++;
    }

    void decreseNumber(){
        this.number--;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }
}
